int	ft_isalnum(int c)
{
	unsigned char	arg;

	arg = c;
	if ((arg >= 'A' && arg <= 'Z') || \
		(arg >= 'a' && arg <= 'z') || \
		(arg >= '0' && arg <= '9'))
		return (1);
	else
		return (0);
}
